// Setting up ages
let myAge = 10;
let earlyYears = 2;

// Update earlyYears
earlyYears *= 10.5;

// myAge - 2
let laterYears = myAge - 2;

//Update laterYears
laterYears *= 4;

// Print current values
console.log(`Early Years: ${earlyYears}`);
console.log(`Later Years: ${laterYears}`);

// Calculate dogYears
let myAgeInDogYears = earlyYears + laterYears;

// Name
let name = 'J.C.';

// Print name and dog years age to console
console.log(`Name: ${name} - Dog Age ${myAgeInDogYears}`);
